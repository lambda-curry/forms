// Create a hook for accessing translations
export function useTranslation(locale: string = 'en') {
  // Fallback to English if the locale is not supported
  const currentLocale = translations[locale] ? locale : 'en';
  
  const t = React.useCallback(
    (key: string): string => {
      return translations[currentLocale][key] || key;
    },
    [currentLocale]
  );
  
  return { t };
}

